# Accessing variable values in print function and extracting the string using the index values.

#!/usr/bin/python3


str1 = 'Hello Python!'		
str2 = "Object Oriented Programming"
print ("str1[0]: ", str1[0]) 		# It will print the 0 index value of str1 var
print ("str2[1:5]: ", str2[1:5])	# It will print the index value from 1 to 4

